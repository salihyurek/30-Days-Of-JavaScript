// Use for loop to iterate from 0 to 100 and print the sum of all numbers. // Output: The sum of all numbers from 0 to 100 is 5050.
// turkish: 0'dan 100'e kadar döngüyü tekrarlamak için for döngüsünü kullanın ve tüm sayıların toplamını yazdırın.

let sum = 0
for (let i = 0; i <= 100; i++) {
    sum += i
}
console.log(`The sum of all numbers from 0 to 100 is ${sum}`) // Output: The sum of all numbers from 0 to 100 is 5050.