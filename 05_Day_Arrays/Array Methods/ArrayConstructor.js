// Array Constructor

// Array constructor is used to create an array
// Array constructor, bir dizi oluşturmak için kullanılır

const arr = Array() // creates eight empty values
console.log(arr) // []


const arr2 = new Array(1, 2, 3, 4, 5) // most recommended
console.log(arr2) // [1, 2, 3, 4, 5]

const eightEmpptyValues = Array(8) // creates eight empty values
console.log(eightEmpptyValues) // [empty × 8]
